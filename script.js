function userCredentials(){
    var name = document.getElementById("name");
    var user = document.getElementById("user");
    var email = document.getElementById("email");
    var pass = document.getElementById("pass");
    var address = document.getElementById("address");
    var gender = document.getElementById("gender");
    var interests = document.getElementById("interests");

    document.getElementById("c-name").innerHTML = "Name: " + name;
    document.getElementById("c-user").innerHTML = "Username: " + user;
    document.getElementById("c-email").innerHTML = "Email: " + email;
    document.getElementById("c-pass").innerHTML = "Password: " + pass;
    document.getElementById("c-address").innerHTML = "Address: " + address;
    document.getElementById("c-gender").innerHTML = "Gender: " + gender;
    document.getElementById("c-interests").innerHTML = "Interests: " + interests;
}
